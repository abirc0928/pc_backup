const doubleIt = num => num * 2;
const add = (x, y) => x + y;
const give5 = () => 5;
const doMath = (x, y) =>{
    const sum = x + y;
    const diff = x - y;
    const result = sum * diff;
    return result ;
}

console.log(doubleIt(5))
console.log(add(50,20))
console.log(give5())
console.log(doMath(7,9))