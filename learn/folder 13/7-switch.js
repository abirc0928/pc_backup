var num = 1000;
switch(num){
    case 100:
        console.log("it is 100");
        break;
    case 80:
        console.log("it is 80");
        break;
    case 50:
        console.log("it is 50");
        break;
    case 20:
    case 10:
        console.log("it is 20, 10");
        break;
    default:
        console.log('None of these');
}